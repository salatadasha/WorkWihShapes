package org.itstep;

public class Triangle extends Shape {

	private int hight;
	private int baseLine;
	public int getHight() {
		return hight;
	}
	public void setHight(int hight) {
		this.hight = hight;
	}
	public int getBaseLine() {
		return baseLine;
	}
	public void setBaseLine(int baseLine) {
		this.baseLine = baseLine;
	}
	
}
